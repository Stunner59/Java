class Float
{
public static void main(String[] args)
{
	System.out.println("Hello Sarwesh");

	//byte to all
	float i = 1.0f;
	byte a = (byte)i;
	short b = (short)i;
	int c = (int)i;
	long d = (long)i;
	double e = (double)i;
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
		
}
}
